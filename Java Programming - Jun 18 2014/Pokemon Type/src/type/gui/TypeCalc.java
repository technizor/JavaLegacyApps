	private void checkTypeAdvantage()
	{
		double[] typeAdvantage = new double[18];
		if (currentType2 != -1)
			for (int type = 0; type < 18; type++)
				typeAdvantage[type] = 1.0
						* typeEffectiveness[type][currentType1]
						* typeEffectiveness[type][currentType2];
		else
			for (int type = 0; type < 18; type++)
				typeAdvantage[type] = 1.0 * typeEffectiveness[type][currentType1];
		displayTypeAdvantage(typeAdvantage);
		return;
	}

	private void displayTypeAdvantage(double[] typeAdvantage)
	{
		ArrayList<Integer> quad = new ArrayList<Integer>();
		ArrayList<Integer> twice = new ArrayList<Integer>();
		ArrayList<Integer> one = new ArrayList<Integer>();
		ArrayList<Integer> half = new ArrayList<Integer>();
		ArrayList<Integer> quarter = new ArrayList<Integer>();
		ArrayList<Integer> immune = new ArrayList<Integer>();
		double advPar = -18.0;
		for (int type = 0; type < 18; type++) {
			advPar += typeAdvantage[type];
			if (typeAdvantage[type] == 4.0)
				quad.add(type);
			else if (typeAdvantage[type] == 2.0)
				twice.add(type);
			else if (typeAdvantage[type] == 1.0)
				one.add(type);
			else if (typeAdvantage[type] == 0.5)
				half.add(type);
			else if (typeAdvantage[type] == 0.25)
				quarter.add(type);
			else if (typeAdvantage[type] == 0.0)
				immune.add(type);
		}

		output.removeAll();
		JPanel sectionPanel = new JPanel();
		sectionPanel
				.setLayout(new BoxLayout(sectionPanel, BoxLayout.PAGE_AXIS));

		ArrayList<JPanel> sections = new ArrayList<JPanel>();
		if (quad.size() > 0)
			sections.add(displaySection("4.0x Damage", quad));
		if (twice.size() > 0)
			sections.add(displaySection("2.0x Damage", twice));
		if (one.size() > 0)
			sections.add(displaySection("1.0x Damage", one));
		if (half.size() > 0)
			sections.add(displaySection("0.5x Damage", half));
		if (quarter.size() > 0)
			sections.add(displaySection("0.25x Damage", quarter));
		if (immune.size() > 0)
			sections.add(displaySection("Immune", immune));

		for (JPanel section : sections)
			sectionPanel.add(section);
		output.add(sectionPanel, BorderLayout.NORTH);
		advantagePar.setText("Advantage Par: " + advPar);

		refreshWindow();
	}

	private JPanel displaySection(String header, ArrayList<Integer> typeList)
	{
		JPanel section = new JPanel();
		section.setLayout(new BorderLayout());
		JLabel headerLabel = new JLabel(header);
		headerLabel.setHorizontalAlignment(SwingConstants.CENTER);
		section.add(headerLabel, BorderLayout.NORTH);

		JPanel panel = new JPanel();
		panel.setLayout(new GridLayout(typeList.size() / 4
				+ (typeList.size() % 4 == 0 ? 0 : 1), 4));
		int w = typeImage.getWidth();
		int h = typeImage.getHeight() / 18;
		for (int type : typeList)
			panel.add(new JLabel(new ImageIcon(typeImage.getSubimage(0, h
					* type, w, h))));
		section.add(panel, BorderLayout.SOUTH);

		return section;
	}

	public void actionHandler(Object source)
	{
		if (source.equals(check))
			checkTypeAdvantage();
		else if (source.equals(type1) || source.equals(type2)) {
			currentType1 = type1.getSelectedIndex();
			currentType2 = type2.getSelectedIndex() - 1;
			if (currentType1 == currentType2)
				check.setEnabled(false);
			else
				check.setEnabled(true);
		}
	}

